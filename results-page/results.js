function mungeData(array) {
    const names = [];
    const quantity = [];
    const caught = [];
    for (let i = 0; i < array.length; i++) {
        const currentArrayItem = array[i];
        names.push(currentArrayItem.name);
        quantity.push(currentArrayItem.quantity);
        caught.push(currentArrayItem.captured);
        
    }
    return [names, quantity, caught];
}
